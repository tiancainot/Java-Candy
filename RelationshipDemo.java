package practice;

public class RelationshipDemo {
    public static void main(Str[] args){
        int i=10;
        int j=20;
        int k=10;
        System.out.println(i==j);
        System.out.println(i==k);
        System.out.println("--------");
        System.out.println(i!=j);
        System.out.println(i!=k);//关系运算符结果都是bool类型
    }
}
